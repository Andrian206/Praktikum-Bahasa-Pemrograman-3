fun main() {
    //penggunaan keyword var mutable bisa diubah
    val nama : String = "Dede" //contoh penulisan dengan tipe data
    val asal = "Kuningan" //contoh penulisan tanpa tipe data
    val usia = 25

    // nilai variable yang diubah
    nama = "Dede Husen"
    asal = "Kuningan Jawa Barat"
    usia = 30

    println(nama)
    println(asal)
    println(usia)
}